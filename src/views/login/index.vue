<template>
  <div class="login-container">
    <el-form
      ref="loginForm"
      :model="loginForm"
      class="login-form"
      auto-complete="on"
      label-position="left"
    >
      <div class="title-container">
        <h3 class="title">登录</h3>
      </div>

      <el-form-item prop="username">
        <span class="icon-container">
          <el-icon><House /></el-icon>
        </span>
        <el-input
          ref="username"
          v-model="loginForm.username"
          placeholder="Username"
          name="username"
          type="text"
          tabindex="1"
          auto-complete="on"
        />
      </el-form-item>

      <el-form-item prop="password">
        <span class="icon-container">
          <el-icon><House /></el-icon>
        </span>
        <el-input
          ref="password"
          v-model="loginForm.password"
          placeholder="Password"
          name="password"
          tabindex="2"
          auto-complete="on"
          @keyup.enter.native="handleLogin"
        />
        <span class="show-pwd">
          <el-icon><House /></el-icon>
        </span>
      </el-form-item>

      <el-button
        :loading="loading"
        type="primary"
        style="width: 100%; margin-bottom: 30px"
        @click.native.prevent="handleLogin"
      >
        登录
      </el-button>

      <div class="tips">
        <span style="margin-right: 20px">username: admin</span>
        <span>请任意填写密码</span>
      </div>
    </el-form>
  </div>
</template>

<script setup lang="ts" name="Login">
import { ref, reactive } from "vue";

// 从hooks中导入数据
import useStoreCase from "@/hooks/store";
import useRouteCase from "@/hooks/route";
const { userStore } = useStoreCase();
const { router } = useRouteCase();

let loading = ref(false);

const loginForm = reactive({
  username: "admin",
  password: "111111",
});
const password = ref();

function handleLogin() {
  loading.value = true;
  userStore
    .login(loginForm)
    .then(() => {
      router.push("/home");
      loading.value = false;
    })
    .catch((error) => {
      console.log("用户登录过程出现错误：", error);
      loading.value = false;
    });
}
</script>

